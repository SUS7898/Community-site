package CafeLove;

public class CafeLoveDTO {
	
	private String num;
	private String id;
	private String title;
	
	public String getNum() {
		
		return num;
	}
	public void setNum(String num) {
		
		this.num = num;
	}
	public String getId() {
		
		return id;
	}
	public void setId(String id) {
		
		this.id = id;
	}
	public String getTitle() {
		
		return title;
	}
	public void setTitle(String title) {
		
		this.title = title;
	}
}